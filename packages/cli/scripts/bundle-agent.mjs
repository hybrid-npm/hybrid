import { cpSync, existsSync, mkdirSync, readdirSync, rmSync } from "node:fs"
import { dirname, resolve } from "node:path"
import { fileURLToPath } from "node:url"
import * as esbuild from "esbuild"

const __dirname = dirname(fileURLToPath(import.meta.url))
const cliDir = resolve(__dirname, "..")
const monorepoDir = resolve(cliDir, "..", "..")
const agentDir = resolve(monorepoDir, "packages", "agent")
const devUiDir = resolve(monorepoDir, "packages", "dev-ui")
const outDir = resolve(cliDir, "dist")

console.log("\n📦 Bundling hybrid package...")

// Ensure output directories exist
mkdirSync(resolve(outDir, "server"), { recursive: true })
mkdirSync(resolve(outDir, "dev-ui"), { recursive: true })

// Build agent runtime
console.log("  Building agent runtime...")
const external = [
	"viem",
	"hono",
	"@hono/node-server",
	"@anthropic-ai/claude-agent-sdk",
	"@xmtp/agent-sdk",
	"@xmtp/node-sdk",
	"@xmtp/node-bindings",
	"sql.js",
	"better-sqlite3",
	"node-llama-cpp",
	"sqlite-vec"
]

await esbuild.build({
	entryPoints: [resolve(agentDir, "src/index.ts")],
	bundle: true,
	platform: "node",
	target: "node20",
	outfile: resolve(outDir, "index.cjs"),
	format: "cjs",
	external,
	banner: { js: "// @generated by hybrid build" }
})

await esbuild.build({
	entryPoints: [resolve(agentDir, "src/server/index.ts")],
	bundle: true,
	platform: "node",
	target: "node20",
	outfile: resolve(outDir, "server/index.cjs"),
	format: "cjs",
	external
})

await esbuild.build({
	entryPoints: [resolve(agentDir, "src/xmtp.ts")],
	bundle: true,
	platform: "node",
	target: "node20",
	outfile: resolve(outDir, "xmtp.cjs"),
	format: "cjs",
	external
})

console.log("  ✓ Agent runtime built")

// Build and copy dev UI
console.log("  Building dev UI...")
const devUiOutDir = resolve(outDir, "dev-ui")

// Run vite build in dev-ui directory
const { execSync } = await import("node:child_process")
execSync("vite build", { cwd: devUiDir, stdio: "pipe" })

// Copy the built assets
if (existsSync(resolve(devUiDir, "dist"))) {
	rmSync(resolve(devUiOutDir), { recursive: true, force: true })
	mkdirSync(devUiOutDir, { recursive: true })
	cpSync(resolve(devUiDir, "dist"), resolve(devUiOutDir, "public"), {
		recursive: true
	})
	console.log("  ✓ Dev UI built and copied")
} else {
	console.log("  ⚠️ Dev UI dist not found")
}

// Copy core skills
console.log("  Copying core skills...")
const skillsDir = resolve(agentDir, "skills")
const targetSkillsDir = resolve(cliDir, "skills")

if (existsSync(skillsDir)) {
	const skills = readdirSync(skillsDir, { withFileTypes: true })
		.filter((d) => d.isDirectory())
		.map((d) => d.name)

	for (const skill of skills) {
		const src = resolve(skillsDir, skill)
		const dest = resolve(targetSkillsDir, skill)
		cpSync(src, dest, { recursive: true })
	}
	console.log(`  ✓ Copied ${skills.length} skills: ${skills.join(", ")}`)
}

// Copy templates
console.log("  Copying templates...")
const templatesDir = resolve(cliDir, "templates")
if (existsSync(templatesDir)) {
	console.log(`  ✓ Templates ready`)
}

// Generate package metadata
const pkgJson = {
	name: "hybrid",
	version: "1.0.0",
	type: "module",
	main: "./dist/index.cjs"
}

console.log("\n✅ Bundle complete!")
console.log(`   Output: ${outDir}`)
console.log(`   Skills: ${targetSkillsDir}`)
console.log(`   Templates: ${templatesDir}`)
